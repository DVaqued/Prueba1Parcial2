    public void imprimirLogs(String[] logs) {
        DefaultListModel<String> listModel = new DefaultListModel<>();
        if (logs != null) {
            for (String log : logs) {
                if (log != null) {
                    listModel.addElement(log);
                }
            }
            jList1.setModel(listModel);
        }
    }
